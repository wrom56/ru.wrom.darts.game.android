package ru.wrom.darts.game.core.api;

public class PlayerSettings {

	private Player player;
	private Dart dart;

	public Player getPlayer() {
		return player;
	}

	public void setPlayer(Player player) {
		this.player = player;
	}

	public Dart getDart() {
		return dart;
	}

	public void setDart(Dart dart) {
		this.dart = dart;
	}
}



